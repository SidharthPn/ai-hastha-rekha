function applyTranslations(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      if (el.tagName.toLowerCase() === 'input' && el.type === 'button') {
        el.value = translations[lang][key];
      } else {
        el.innerHTML = translations[lang][key];
      }
    }
  });

  const toggler = document.getElementById('langToggler');
  if (toggler) {
    toggler.textContent = lang === 'en' ? 'മലയാളം' : 'English';
  }
}

function toggleLang() {
  const currentLang = localStorage.getItem('astro_lang_preference') || 'en';
  const newLang = currentLang === 'en' ? 'ml' : 'en';
  localStorage.setItem('astro_lang_preference', newLang);

  // Custom logic to reload logic if app expects it, or just apply
  applyTranslations(newLang);

  // If in Palm App, trigger backend UI switch if possible without refresh, 
  // or just reload to get proper readings. Let's trigger custom events:
  document.dispatchEvent(new CustomEvent('langChanged', { detail: newLang }));
}

// On load
document.addEventListener('DOMContentLoaded', () => {
  const currentLang = localStorage.getItem('astro_lang_preference') || 'en';
  applyTranslations(currentLang);

  const toggler = document.getElementById('langToggler');
  if (toggler) {
    toggler.addEventListener('click', toggleLang);
  }
});

window.translations = translations;
